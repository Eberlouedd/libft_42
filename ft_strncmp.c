int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while ((s1[i] || s2[i]) && n > 0)
	{
		if (s1[i] != s2[i])
		{
			return ((unsigned int)s1[i] - (unsigned int)s2[i]);
		}
		i++;
		n--;
	}
	return (0);
}
